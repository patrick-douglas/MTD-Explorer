#!/usr/bin/env Rscript

# VERSION: 3.0.0
# OUTPUTS: multi-group venn_diagramm.pdf + euler_diagramm.pdf
# DEFAULT: --plot_type both; Euler accepts 2 or more groups

# MTD Explorer
# High-quality Venn and proportional Euler diagrams for HUMAnN
# GO/KEGG functional features.
#
# The group assignments always come from the mandatory samplesheet.
# Optional extended metadata is intentionally not used.
#
# Presence rule (matching DEG_Anno_Plot.R):
# a feature is present in a group when its normalized abundance is
# greater than the selected threshold in at least one sample from that group.

