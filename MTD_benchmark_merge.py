#!/usr/bin/env python3
"""
Merge MTD installation benchmark runs into article-ready TSV tables.

The script recursively finds directories containing summary.tsv generated by
MTD_benchmark_install.sh. When steps.tsv exists, function-level timings are
also merged and summarized.

Example:
    python3 MTD_benchmark_merge.py \
        --input ~/MTD_benchmarks \
        --input /mnt/secondary_machine_benchmarks \
        --output ~/MTD_benchmark_merged
"""

from __future__ import annotations

import argparse
import csv
import re
import statistics
import sys
from collections import defaultdict
from pathlib import Path
from typing import Any, Iterable


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Merge MTD installation benchmark runs."
    )
    parser.add_argument(
        "--input",
        action="append",
        required=True,
        help="Benchmark root directory. Repeat for multiple machines.",
    )
    parser.add_argument(
        "--output",
        required=True,
        help="Directory where merged TSV files will be written.",
    )
    return parser.parse_args()


def read_tsv(path: Path) -> list[dict[str, str]]:
    with path.open(newline="", encoding="utf-8", errors="replace") as handle:
        return list(csv.DictReader(handle, delimiter="\t"))


def write_tsv(path: Path, rows: Iterable[dict[str, Any]], columns: list[str]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", newline="", encoding="utf-8") as handle:
        writer = csv.DictWriter(
            handle,
            fieldnames=columns,
            delimiter="\t",
            extrasaction="ignore",
            lineterminator="\n",
        )
        writer.writeheader()
        for row in rows:
            writer.writerow({column: row.get(column, "") for column in columns})


def parse_summary(path: Path) -> dict[str, str]:
    result: dict[str, str] = {}
    for row in read_tsv(path):
        metric = row.get("metric", "")
        if metric:
            result[metric] = row.get("value", "")
    return result


def first_regex(text: str, patterns: list[str]) -> str:
    for pattern in patterns:
        match = re.search(pattern, text, flags=re.MULTILINE)
        if match:
            return match.group(1).strip().strip('"')
    return ""


def parse_hardware(run_dir: Path) -> dict[str, str]:
    path = run_dir / "hardware.txt"
    if not path.exists():
        return {}
    text = path.read_text(encoding="utf-8", errors="replace")

    cpu_model = first_regex(
        text,
        [
            r"^Model name:\s*(.+)$",
            r"^Hardware:\s*(.+)$",
            r"^model name\s*:\s*(.+)$",
        ],
    )
    logical_cpus = first_regex(
        text,
        [
            r"^CPU\(s\):\s*([0-9]+)\s*$",
            r"^logical_cpus:\s*([0-9]+)\s*$",
        ],
    )
    mem_kib = first_regex(text, [r"^MemTotal:\s*([0-9]+)\s+kB$"])
    os_name = first_regex(text, [r"^PRETTY_NAME=(.+)$"])
    kernel = first_regex(text, [r"^kernel:\s*(.+)$"])

    total_ram_gib = ""
    if mem_kib:
        total_ram_gib = f"{int(mem_kib) / 1024 / 1024:.3f}"

    return {
        "os": os_name,
        "kernel": kernel,
        "cpu_model": cpu_model,
        "logical_cpus": logical_cpus,
        "total_ram_gib": total_ram_gib,
    }


def parse_git(run_dir: Path) -> dict[str, str]:
    path = run_dir / "git_state.txt"
    if not path.exists():
        return {}
    text = path.read_text(encoding="utf-8", errors="replace")
    return {
        "git_commit": first_regex(text, [r"^commit:\s*(\S+)$"]),
        "git_branch": first_regex(text, [r"^branch:\s*(.*)$"]),
        "git_describe": first_regex(text, [r"^describe:\s*(.*)$"]),
    }


def numeric(value: str) -> float | None:
    try:
        return float(value)
    except (TypeError, ValueError):
        return None


def hours(value: str) -> str:
    number = numeric(value)
    return "" if number is None else f"{number / 3600:.6f}"


def collect_runs(input_roots: list[Path]) -> tuple[list[dict[str, str]], list[dict[str, str]]]:
    runs: list[dict[str, str]] = []
    steps: list[dict[str, str]] = []
    seen_summary_paths: set[Path] = set()

    for root in input_roots:
        if not root.exists():
            raise FileNotFoundError(f"Input directory does not exist: {root}")

        for summary_path in sorted(root.rglob("summary.tsv")):
            resolved = summary_path.resolve()
            if resolved in seen_summary_paths:
                continue
            seen_summary_paths.add(resolved)

            run_dir = summary_path.parent
            row = parse_summary(summary_path)
            row["run_directory"] = str(run_dir.resolve())
            row.update(parse_hardware(run_dir))
            row.update(parse_git(run_dir))
            row["wall_time_hours"] = hours(row.get("wall_time_seconds", ""))
            runs.append(row)

            steps_path = run_dir / "steps.tsv"
            if steps_path.exists():
                for step in read_tsv(steps_path):
                    step["run_directory"] = str(run_dir.resolve())
                    steps.append(step)

    return runs, steps


def wide_columns(rows: list[dict[str, str]], preferred: list[str]) -> list[str]:
    all_columns = {key for row in rows for key in row}
    remaining = sorted(all_columns - set(preferred))
    return [column for column in preferred if column in all_columns] + remaining


def summarize_steps(steps: list[dict[str, str]]) -> list[dict[str, Any]]:
    grouped: dict[tuple[str, str, str, str], list[dict[str, str]]] = defaultdict(list)

    for row in steps:
        key = (
            row.get("run_id", ""),
            row.get("label", ""),
            row.get("host", ""),
            row.get("function", ""),
        )
        grouped[key].append(row)

    summary: list[dict[str, Any]] = []
    for (run_id, label, host, function), rows in sorted(grouped.items()):
        elapsed_values = [
            value
            for value in (numeric(row.get("elapsed_seconds", "")) for row in rows)
            if value is not None
        ]
        statuses = [
            int(value)
            for value in (numeric(row.get("status", "")) for row in rows)
            if value is not None
        ]
        depths = [
            int(value)
            for value in (numeric(row.get("call_depth", "")) for row in rows)
            if value is not None
        ]

        if not elapsed_values:
            continue

        summary.append(
            {
                "run_id": run_id,
                "label": label,
                "host": host,
                "function": function,
                "calls": len(elapsed_values),
                "failed_calls": sum(status != 0 for status in statuses),
                "minimum_call_depth": min(depths) if depths else "",
                "total_elapsed_seconds": f"{sum(elapsed_values):.6f}",
                "mean_elapsed_seconds": f"{statistics.mean(elapsed_values):.6f}",
                "median_elapsed_seconds": f"{statistics.median(elapsed_values):.6f}",
                "minimum_elapsed_seconds": f"{min(elapsed_values):.6f}",
                "maximum_elapsed_seconds": f"{max(elapsed_values):.6f}",
            }
        )
    return summary


def curated_article_rows(runs: list[dict[str, str]]) -> list[dict[str, str]]:
    mapping = {
        "mean_system_cpu_busy_percent": "mean_system_cpu_busy",
        "peak_system_cpu_busy_percent": "peak_system_cpu_busy",
        "peak_process_tree_rss_gib": "peak_process_tree_rss",
        "peak_system_memory_used_gib": "peak_system_memory_used",
        "total_disk_read_gib": "total_physical_disk_read",
        "total_disk_write_gib": "total_physical_disk_write",
        "total_network_received_gib": "total_network_received",
        "total_network_transmitted_gib": "total_network_transmitted",
        "peak_temperature_c": "peak_reported_temperature",
    }

    result: list[dict[str, str]] = []
    for run in runs:
        row = {
            "run_id": run.get("run_id", ""),
            "label": run.get("label", ""),
            "host": run.get("host", ""),
            "os": run.get("os", ""),
            "kernel": run.get("kernel", ""),
            "cpu_model": run.get("cpu_model", ""),
            "logical_cpus": run.get("logical_cpus", ""),
            "total_ram_gib": run.get("total_ram_gib", ""),
            "git_commit": run.get("git_commit", ""),
            "exit_status": run.get("exit_status", ""),
            "wall_time_hours": run.get("wall_time_hours", ""),
            "run_directory": run.get("run_directory", ""),
        }
        for destination, source in mapping.items():
            row[destination] = run.get(source, "")
        result.append(row)
    return result


def main() -> int:
    args = parse_args()
    input_roots = [Path(value).expanduser().resolve() for value in args.input]
    output_dir = Path(args.output).expanduser().resolve()
    output_dir.mkdir(parents=True, exist_ok=True)

    try:
        runs, steps = collect_runs(input_roots)
    except (OSError, ValueError) as exc:
        print(f"[ERROR] {exc}", file=sys.stderr)
        return 1

    if not runs:
        print("[ERROR] No summary.tsv files were found.", file=sys.stderr)
        return 1

    preferred_run_columns = [
        "run_id",
        "label",
        "host",
        "start_utc",
        "end_utc",
        "exit_status",
        "wall_time_seconds",
        "wall_time_hours",
        "os",
        "kernel",
        "cpu_model",
        "logical_cpus",
        "total_ram_gib",
        "git_commit",
        "git_branch",
        "git_describe",
        "run_directory",
    ]
    run_columns = wide_columns(runs, preferred_run_columns)
    write_tsv(output_dir / "runs_wide.tsv", runs, run_columns)

    article_rows = curated_article_rows(runs)
    article_columns = [
        "run_id",
        "label",
        "host",
        "os",
        "kernel",
        "cpu_model",
        "logical_cpus",
        "total_ram_gib",
        "git_commit",
        "exit_status",
        "wall_time_hours",
        "mean_system_cpu_busy_percent",
        "peak_system_cpu_busy_percent",
        "peak_process_tree_rss_gib",
        "peak_system_memory_used_gib",
        "total_disk_read_gib",
        "total_disk_write_gib",
        "total_network_received_gib",
        "total_network_transmitted_gib",
        "peak_temperature_c",
        "run_directory",
    ]
    write_tsv(output_dir / "runs_article.tsv", article_rows, article_columns)

    if steps:
        step_columns = wide_columns(
            steps,
            [
                "run_id",
                "label",
                "host",
                "function",
                "parent_function",
                "call_depth",
                "start_utc",
                "end_utc",
                "elapsed_seconds",
                "status",
                "pid",
                "run_directory",
            ],
        )
        write_tsv(output_dir / "steps_long.tsv", steps, step_columns)

        step_summary = summarize_steps(steps)
        step_summary_columns = [
            "run_id",
            "label",
            "host",
            "function",
            "calls",
            "failed_calls",
            "minimum_call_depth",
            "total_elapsed_seconds",
            "mean_elapsed_seconds",
            "median_elapsed_seconds",
            "minimum_elapsed_seconds",
            "maximum_elapsed_seconds",
        ]
        write_tsv(
            output_dir / "steps_summary.tsv",
            step_summary,
            step_summary_columns,
        )

    print(f"[OK] Runs merged: {len(runs)}")
    print(f"[OK] Function calls merged: {len(steps)}")
    print(f"[OK] Output directory: {output_dir}")
    print(f"[OK] Main article table: {output_dir / 'runs_article.tsv'}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
